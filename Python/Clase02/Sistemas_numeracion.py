# Profundizando en los sistemas de númeración.

    #SISTEMA DECIMAL.
a = 10
print(f"a decimal: {a}")
    #SISTEMA BINARIO.
a = 0b1010
print(f"a binario: {a}")
    #SISTEMA OCTAL.
a = 0o12
print(f"a octal: {a}")
    #SISTEMA HEXADECIMAL.
a = 0xA
print(f"a hexadecimal: {a}")

    #BASE DECIMAL.
a = int('23', 10)
print(f"a base decimal: {a}")

    #BASE BINARIA.
a = int('1010', 2)
print(f"a base binario: {a}")

    #BASE OCTAL.
a = int('12', 8)
print(f'a base octal: {a}')

    #BASE HEXADECIMAL.
a = int('A', 16)
print(f'a base hexadecimal: {a}')

    #BASE 5 SUS VALORES SON DE 4 A 0.
a = int('34', 5)
print(f'a = base 5: {a}')